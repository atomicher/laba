#include <stdio.h>

int main() {

    // Оголошення масиву та ініціалізація значеннями
    int arr[] = { 1, 2, 3, 4, 5 };

    // Обчислення розміру масиву (кількості елементів) (використовувала GPT)
    int n = sizeof(arr) / sizeof(arr[0]);

    // Ініціалізація змінної sum, яка буде накопичувачем суми
    int sum = 0;

    // Цикл перебору всіх елементів масиву
    for (int i = 0; i < n; ++i) {

        // Додавання поточного елементу до накопичувача суми
        sum += arr[i];
    }

    // Виведення підрахованої суми всіх елементів 
    printf("The sum of array elements: %d\n", sum);

    return 0;
}
